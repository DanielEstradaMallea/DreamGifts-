    private void BuscarInformeComunaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarInformeComunaActionPerformed

        modeloDespachos.setRowCount(0);
        int total = 0, contador = 0;
        Date date = InformeComunas1.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");
        String fechaVenta1 = (String.valueOf(sdf.format(date)));
        Date date1 = InformeComunas2.getDate();
        String fechaVenta2 = (String.valueOf(sdf.format(date1)));

        System.out.println(fechaVenta1);

        try {

            con.connections();

            String sql = ("SELECT C.pack_nombre,V.VTA_CANTIDAD, COUNT(*)\n"
                    + "CONTADOR FROM venta V \n"
                    + "INNER JOIN pack C ON C.id_pack=V.PCK_ID_PACK \n"
                    + "WHERE v.VTA_FECHA_ENTREGA BETWEEN '" + fechaVenta1 + "' AND '" + fechaVenta2 + "'\n"
                    + "GROUP BY C.pack_nombre\n"
                    + "ORDER BY CONTADOR");
            PreparedStatement pstm = con.conexion.prepareStatement(sql);
            ResultSet respuesta1 = pstm.executeQuery();

            while (respuesta1.next()) {

//                
                Object[] dato = {
                    respuesta1.getString("pack_nombre"),
                    respuesta1.getString("CONTADOR"),
                    (respuesta1.getInt("CONTADOR")) * respuesta1.getInt("VTA_CANTIDAD")};

                modeloDespachos.addRow(dato);
                total = (respuesta1.getInt("CONTADOR")) * respuesta1.getInt("VTA_CANTIDAD");

                contador = total + contador;
            }
            txtTotal.setText("$"+String.valueOf(contador));

        } catch (Exception e) {
            System.out.println("no se listo pendientes por error : " + e);
        }
    }//GEN-LAST:event_BuscarInformeComunaActionPerformed


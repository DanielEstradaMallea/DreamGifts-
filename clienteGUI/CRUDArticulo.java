    void categorias() {

        try {
            con.connections();
            PreparedStatement pstm = con.conexion.prepareStatement("select * from categoria");
            ResultSet respuesta = pstm.executeQuery();

            while (respuesta.next()) {

                cmbCategoria.addItem(respuesta.getString("cat_descripcion"));
            }

        } catch (SQLException e) {
        }

    }

    void proveedores() {

        try {
            con.connections();
            PreparedStatement pstm = con.conexion.prepareStatement("select * from proveedor");
            ResultSet respuesta = pstm.executeQuery();

            while (respuesta.next()) {

                cmbProveedor.addItem(respuesta.getString("prov_razonsocial"));
            }

        } catch (SQLException e) {
        }

    }


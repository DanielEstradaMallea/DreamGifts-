    private void btnGuardarArticuloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarArticuloActionPerformed
        articuloUsecase articuloObjetc = new articuloUsecase();
        articulo articuloRequest = new articulo();

        String formato = JDfechaVencimiento.getDateFormatString();
        Date date = JDfechaVencimiento.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");
        String fechaV =(String.valueOf(sdf.format(date)));
        
        int a = (int)txtStock.getValue();

//        System.out.println(cmbCategoria.getSelectedIndex());
//        System.out.println(cmbProveedor.getSelectedIndex());
//        System.out.println(txtNombreArticulo.getText());
////        System.out.println(Integer.parseInt(jtxtStock.getText()));
//        System.out.println((JVencimiento.getText()));
        articuloRequest.setDescripcionArticulo(txtNombreArticulo.getText());
        articuloRequest.setIdCatArticulo(((cmbCategoria.getSelectedIndex())));
        articuloRequest.setStockArticulo(a);
        articuloRequest.setFechaVencArticulo(fechaV);//
        articuloRequest.setIdProvArticulo((cmbProveedor.getSelectedIndex()));

        try {
            boolean respuesta = articuloObjetc.registrarArticulo(articuloRequest);
            if (respuesta) {

                listarArticulos();
                clear();

            }
        } catch (Exception ex) {

            System.out.println("boton guardar con problemas");
            Logger.getLogger(CRUDArticulo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarArticuloActionPerformed


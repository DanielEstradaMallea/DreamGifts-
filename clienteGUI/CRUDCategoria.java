    private void btnGuardarCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCategoriaActionPerformed
        categoriaUsecase categoria = new categoriaUsecase();
        categoria categoriaRequest = new categoria();
        categoriaRequest.setCategoria(txtCategoria.getText());

        try {
            boolean respuesta = categoria.registrarCategoria(categoriaRequest);
            if (respuesta) {

                listarCategorias();
            }
        } catch (Exception ex) {
            Logger.getLogger(CRUDCategoria.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarCategoriaActionPerformed


    private void tblCategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCategoriaMouseClicked
        categoriaUsecase updateCategoria = new categoriaUsecase();
        int fila = tblCategoria.rowAtPoint(evt.getPoint());
        int columna = tblCategoria.columnAtPoint(evt.getPoint());
        JTable r = (JTable) evt.getSource();
        if (columna == 3) {
            int op = JOptionPane.showConfirmDialog(null, "Desea cambiar el estado", "Confirmar", JOptionPane.OK_CANCEL_OPTION);
            if (op == 0) {

                String codigo = r.getModel().getValueAt(r.getSelectedRow(), 1).toString();
                String estado = r.getModel().getValueAt(r.getSelectedRow(), 2).toString();

                try {
                    updateCategoria.updateEstadoCategoria(codigo, estado);
                    listarCategorias();
                } catch (Exception ex) {
                    Logger.getLogger(CRUDCategoria.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        if (columna == 4) {
            System.out.println("Click en el boton editar");
            editarBanco.setVisible(true);

            txtEditarNombre.setText(r.getModel().getValueAt(r.getSelectedRow(), 0).toString());
        }
        txtEditarCodigo.setText(r.getModel().getValueAt(r.getSelectedRow(), 1).toString());
    }//GEN-LAST:event_tblCategoriaMouseClicked

    private void txtBuscarCategoriaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarCategoriaKeyTyped
        txtBuscarCategoria.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent ke) {
                trs.setRowFilter(RowFilter.regexFilter("(?i)" + txtBuscarCategoria.getText(), 0));
            }

        });
        trs = new TableRowSorter(dtmBanco);
        tblCategoria.setRowSorter(trs);
        if (txtBuscarCategoria.getText().equals("")) {
            try {
                listarCategorias();
            } catch (Exception ex) {
                Logger.getLogger(CRUDCategoria.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_txtBuscarCategoriaKeyTyped

